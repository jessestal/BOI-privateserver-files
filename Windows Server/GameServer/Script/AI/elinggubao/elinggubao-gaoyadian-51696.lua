function Event_OnTimer(nAI,nIndex, nCount)
        local AI = GetMonsterAI(nAI)
        
        if nIndex == 0 then
        --释放技能
            AI:UseSkill(2047,1)
        end 
        
          if nIndex == 1 then
			--如果是普通难度才召电流怪
		c = AI:GetMapID()
		d = AI:GetEctypeVarInteger(c, 110)--队伍平均等级
		e = AI:GetEctypeVarInteger(c, 111)--副本难度
			
		x = AI:GetPosX()
		y = AI:GetPosY()
		if e == 1 then
			AI:SummonMonsterByPos(51699,x ,y )--召唤电流怪
		end
	end
end


