	a = AI:GetArray(4)
	if a == 0 then
		AI:SetTimer(0,500,65535)
		AI:AddSkill(2047,1)
     		AI:SetArray(4,1)
    	end
	
	--每60秒召唤一个电流怪
	b = AI:GetArray(1)
	if b == 0 then
		AI:SetTimer(1,60000,65535)
		AI:SetArray(1,1)
	end
end

